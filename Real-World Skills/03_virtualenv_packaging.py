# Topic: Python Virtual Environments & Packaging
# Shows how to create a virtual environment and install packages

# Step 1: Create a virtual environment
# In terminal: python3 -m venv myenv

# Step 2: Activate it
# Linux/macOS: source myenv/bin/activate
# Windows: myenv\Scripts\activate

# Step 3: Install a package (example: requests)
# pip install requests

# Step 4: Freeze requirements
# pip freeze > requirements.txt

print("Virtual environment setup is done. Use pip inside your env to manage packages.")
