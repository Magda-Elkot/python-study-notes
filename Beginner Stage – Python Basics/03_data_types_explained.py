# Topic: Integers, Floats, Strings, Booleans

# INTEGER (int)
# Whole numbers, positive or negative, no decimal point
my_age = 30
print("my_age:", my_age, "| Type:", type(my_age))

# FLOAT (float)
# Numbers with a decimal point
temperature = 36.6
print("temperature:", temperature, "| Type:", type(temperature))

# STRING (str)
# Text values, written in quotes
greeting = "Hello, world!"
print("greeting:", greeting, "| Type:", type(greeting))

# You can use single or double quotes for strings
quote1 = "Python is fun"
quote2 = 'So is learning!'
print(quote1, "|", quote2)

# BOOLEAN (bool)
# Only two values: True or False (case-sensitive!)
is_python_fun = True
is_raining = False
print("is_python_fun:", is_python_fun, "| Type:", type(is_python_fun))
print("is_raining:", is_raining, "| Type:", type(is_raining))

# Mixing types in print statements
print("I am", my_age, "years old.")
print("Is it raining today?", is_raining)
